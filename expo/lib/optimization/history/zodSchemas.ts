import { z } from 'zod';

export const DataAuthoritySchema = z.enum([
  'closeout-verified',
  'closeout-user-entered',
  'imported',
  'session-rollup',
  'calculated',
  'estimated',
  'generated',
  'missing',
]);

export const ConfidenceBandSchema = z.enum(['high', 'medium', 'low', 'missing']);
export const OptimizationCasinoBrandSchema = z.enum(['royal', 'celebrity', 'carnival', 'silversea', 'unknown']);
export const OptimizationCasinoProgramSchema = z.enum(['club-royale', 'blue-chip', 'players-club', 'venetian-society', 'unknown']);

export const FieldAuthoritySchema = z.object({
  value: z.unknown().nullable(),
  source: z.string(),
  authority: DataAuthoritySchema,
  confidence: ConfidenceBandSchema,
  confidenceScore: z.number().min(0).max(1),
  freshness: z.string().nullable(),
  warnings: z.array(z.string()),
});

export const CasinoSessionObservationSchema = z.object({
  id: z.string(),
  sourceSessionId: z.string(),
  ownerProfileId: z.string(),
  brand: OptimizationCasinoBrandSchema,
  program: OptimizationCasinoProgramSchema,
  cruiseId: z.string().nullable(),
  reconciliationStatus: z.enum(['matched', 'orphan', 'ambiguous', 'duplicate', 'profile-mismatch']),
  reconciliationWarnings: z.array(z.string()),
  sessionSequence: z.number().int().positive().nullable(),
  date: z.string().nullable(),
  startTime: z.string().nullable(),
  endTime: z.string().nullable(),
  durationMinutes: z.number().nullable(),
  pointsEarned: z.number().nullable(),
  pointsAtSessionStart: z.number().nullable(),
  pointsAtSessionEnd: z.number().nullable(),
  cumulativeCruisePointsAfterSession: z.number().nullable(),
  winLoss: z.number().nullable(),
  cumulativeTripResultAfterSession: z.number().nullable(),
  buyIn: z.number().nullable(),
  cashOut: z.number().nullable(),
  coinIn: z.number().nullable(),
  coinOut: z.number().nullable(),
  freePlayUsed: z.number().nullable(),
  estimatedTheo: z.number().nullable(),
  estimatedExpectedLoss: z.number().nullable(),
  casinoDay: z.number().nullable(),
  dayType: z.enum(['sea', 'port', 'private-island', 'embarkation', 'debarkation', 'unknown']),
  machineId: z.string().nullable(),
  machineName: z.string().nullable(),
  machineFamily: z.string().nullable(),
  rtp: z.number().nullable(),
  rtpAuthority: DataAuthoritySchema,
  volatility: z.string().nullable(),
  remainingDailyBankrollBefore: z.number().nullable(),
  remainingDailyBankrollAfter: z.number().nullable(),
  remainingCasinoHoursEstimate: z.number().nullable(),
  fatigueRating: z.number().nullable(),
  stopReason: z.string().nullable(),
  recommendationId: z.string().nullable(),
  source: z.string(),
  authority: DataAuthoritySchema,
  confidence: ConfidenceBandSchema,
  fingerprint: z.string(),
});

export const CasinoCruiseOutcomeSchema = z.object({
  id: z.string(),
  sourceCruiseId: z.string(),
  ownerProfileId: z.string(),
  brand: OptimizationCasinoBrandSchema,
  program: OptimizationCasinoProgramSchema,
  cruiseId: z.string(),
  reservationId: z.string().nullable(),
  shipName: z.string(),
  sailDate: z.string(),
  returnDate: z.string(),
  nights: z.number().nullable(),
  casinoOpenDays: z.number().nullable(),
  casinoOpenHours: z.number().nullable(),
  seaDayCount: z.number().nullable(),
  portDayCount: z.number().nullable(),
  privateIslandDayCount: z.number().nullable(),
  totalPoints: FieldAuthoritySchema,
  totalCoinIn: FieldAuthoritySchema,
  actualResult: FieldAuthoritySchema,
  theoreticalLoss: FieldAuthoritySchema,
  buyIn: FieldAuthoritySchema,
  cashOut: FieldAuthoritySchema,
  freePlayUsed: FieldAuthoritySchema,
  timePlayedMinutes: FieldAuthoritySchema,
  sessionCount: z.number().int().nonnegative(),
  machineMix: z.record(z.string(), z.number().int().nonnegative()),
  averagePointsPerDay: z.number().nullable(),
  averagePointsPerSession: z.number().nullable(),
  averagePointsPerHour: z.number().nullable(),
  certificateEarnedCode: z.string().nullable(),
  thresholdReached: z.number().nullable(),
  certificateEvidence: z.unknown().nullable(),
  certificateValueSnapshotId: z.string().nullable(),
  fieldAuthority: z.record(z.string(), FieldAuthoritySchema),
  dataHealth: z.unknown(),
  thresholdAttempts: z.array(z.unknown()),
  eligibleForModeling: z.boolean(),
  exclusionReasons: z.array(z.string()),
  warnings: z.array(z.string()),
  createdAt: z.string(),
  updatedAt: z.string(),
});
