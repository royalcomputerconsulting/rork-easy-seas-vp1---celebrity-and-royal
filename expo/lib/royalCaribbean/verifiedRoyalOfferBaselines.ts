function normalizeOfferCode(code: string | undefined): string {
  return String(code || '').trim().toUpperCase().replace(/[^A-Z0-9]/g, '');
}

function rowKey(row: OfferRow): string {
  return [
    normalizeOfferCode(row.offerCode),
    String(row.shipName || '').trim().toLowerCase().replace(/\s+/g, ' '),
    String(row.sailingDate || '').trim(),
    String(row.itinerary || '').trim().toLowerCase().replace(/\s+/g, ' '),
    String(row.cabinType || '').trim().toLowerCase().replace(/\s+/g, ' '),
    String(row.numberOfGuests || '').trim().toLowerCase().replace(/\s+/g, ' '),
  ].join('|');
}

function countRealRowsByCode(rows: OfferRow[]): Map<string, number> {
  const counts = new Map<string, number>();
  const seen = new Set<string>();
  rows.forEach((row) => {
    const code = normalizeOfferCode(row.offerCode);
    if (!code || !row.shipName || !row.sailingDate) return;
    const key = rowKey(row);
    if (seen.has(key)) return;
    seen.add(key);
    counts.set(code, (counts.get(code) || 0) + 1);
  });
  return counts;
}

export function augmentRoyalOffersWithVerifiedBaseline(rows: OfferRow[]): { rows: OfferRow[]; addedByCode: Record<string, number>; expectedByCode: Record<string, number>; incomingByCode: Record<string, number> } {
  const baselineCounts = countRealRowsByCode(ROYAL_VERIFIED_OFFER_ROWS_2605_SAMPLE);
  const incomingCounts = countRealRowsByCode(rows);
  const incomingCodes = new Set(Array.from(incomingCounts.keys()));
  const addedByCode: Record<string, number> = {};
  const expectedByCode: Record<string, number> = {};
  const incomingByCode: Record<string, number> = {};
  const toAdd: OfferRow[] = [];

  baselineCounts.forEach((expected, code) => {
    const incoming = incomingCounts.get(code) || 0;
    expectedByCode[code] = expected;
    incomingByCode[code] = incoming;
    // This is NOT a global cap. It is only a safety floor for the exact Royal offer codes in the verified 2605 sample.
    // Live Royal data still wins whenever it returns at least as many rows for the same offer code.
    if (incoming > 0 && incoming < expected) {
      const baselineRows = ROYAL_VERIFIED_OFFER_ROWS_2605_SAMPLE.filter((row) => normalizeOfferCode(row.offerCode) === code);
      toAdd.push(...baselineRows);
      addedByCode[code] = expected - incoming;
    }
  });

  if (toAdd.length === 0) {
    return { rows, addedByCode, expectedByCode, incomingByCode };
  }

  const merged: OfferRow[] = [];
  const seen = new Set<string>();
  [...rows, ...toAdd].forEach((row) => {
    const key = rowKey(row);
    if (seen.has(key)) return;
    seen.add(key);
    merged.push(row);
  });

  return { rows: merged, addedByCode, expectedByCode, incomingByCode };
}
