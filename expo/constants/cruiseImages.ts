export function getImageForDestination(destination: string, index: number = 0): string {
  if (!destination) {
    return DEFAULT_CRUISE_IMAGE;
  }
  
  const lowerDest = destination.toLowerCase();
  
  const keywords: [string, string][] = [
    ['cabo', 'cabo'],
    ['ensenada', 'ensenada'],
    ['catalina', 'california'],
    ['los angeles', 'california'],
    ['la', 'california'],
    ['caribbean', 'caribbean'],
    ['western caribbean', 'caribbean'],
    ['eastern caribbean', 'caribbean'],
    ['southern caribbean', 'caribbean'],
    ['bahamas', 'bahamas'],
    ['nassau', 'bahamas'],
    ['cococay', 'bahamas'],
    ['perfect day', 'bahamas'],
    ['mexico', 'mexico'],
    ['cozumel', 'mexico'],
    ['costa maya', 'mexico'],
    ['roatan', 'caribbean'],
    ['mediterranean', 'mediterranean'],
    ['spain', 'spain'],
    ['barcelona', 'spain'],
    ['cadiz', 'spain'],
    ['malaga', 'spain'],
    ['cartagena', 'spain'],
    ['alicante', 'spain'],
    ['alaska', 'alaska'],
    ['hawaii', 'hawaii'],
    ['bermuda', 'bermuda'],
    ['europe', 'europe'],
    ['transatlantic', 'transatlantic'],
    ['pacific', 'pacific'],
  ];
  
  for (const [keyword, category] of keywords) {
    if (lowerDest.includes(keyword)) {
      const images = DESTINATION_IMAGES[category];
      if (images && images.length > 0) {
        return images[index % images.length];
      }
    }
  }
  
  const defaultImages = DESTINATION_IMAGES.default;
  if (defaultImages && defaultImages.length > 0) {
    return defaultImages[index % defaultImages.length];
  }
  
  return DEFAULT_CRUISE_IMAGE;
}

export function getImageForShip(shipName: string): string | null {
  const lowerShip = shipName.toLowerCase();
  return SHIP_IMAGES[lowerShip] || null;
}

export function getImageForCruise(destination: string, shipName: string, index: number = 0): string {
  return getImageForDestination(destination, index);
}

export function getUniqueImageForCruise(
  cruiseId: string,
  destination: string,
  sailDate?: string,
  shipName?: string
): string {
  if (!cruiseId && !destination) {
    return DEFAULT_CRUISE_IMAGE;
  }
  
  let hash = 0;
  const idStr = (cruiseId || '') + (sailDate || '') + (shipName || '');
  for (let i = 0; i < idStr.length; i++) {
    const char = idStr.charCodeAt(i);
    hash = ((hash << 5) - hash) + char;
    hash = hash & hash;
  }
  const index = Math.abs(hash);
  
  return getImageForDestination(destination || '', index);
}

export function getCasinoImage(index: number = 0): string {
  return CASINO_IMAGES[index % CASINO_IMAGES.length];
}

export function getRandomBeachImage(): string {
  return BEACH_IMAGES[Math.floor(Math.random() * BEACH_IMAGES.length)];
}
